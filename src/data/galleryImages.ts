// Função para obter 5 imagens aleatórias únicas
export function getRandomImages(count: number = 5): typeof galleryImages {
  const shuffled = [...galleryImages].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

// Função para obter próximas 5 imagens (circular)
export function getNextImages(currentIndex: number, count: number = 5): typeof galleryImages {
  const nextIndex = (currentIndex + count) % galleryImages.length;
  if (nextIndex + count <= galleryImages.length) {
    return galleryImages.slice(nextIndex, nextIndex + count);
  }
  // Se não houver imagens suficientes, pega do início
  const remaining = count - (galleryImages.length - nextIndex);
  return [...galleryImages.slice(nextIndex), ...galleryImages.slice(0, remaining)];
}

